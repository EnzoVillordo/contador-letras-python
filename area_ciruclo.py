import math


print("Bienvenido a la calculadora de area de un circulo")

radio_cincunferencia = float(input("Ingrese el radio de la circunferencia en centrimetros: "))

area_cicunferencia = math.pi * (radio_cincunferencia**2)

print("El area del circulo es: ", area_cicunferencia , "centimetros cuadrados")
 